                    <li style="font-weight: 400;"> <i class="bi bi-check2-circle"></i> Abandoned</li>
                </ul></p>    
            </li>
             <li><strong>Correct: Review current legal status appropriately</strong><br><p>Filing a trademark without full search may result in:
