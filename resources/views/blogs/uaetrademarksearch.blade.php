                    <li style="font-weight: 400;"> <i class="bi bi-check2-circle"></i> Legal disputes</li>
                    <li style="font-weight: 400;"> <i class="bi bi-check2-circle"></i> Waste of time and expenses </li>
                </ul></p> 
                <p>A full trademark search would minimize risk and maximize chances of approval.</p>
