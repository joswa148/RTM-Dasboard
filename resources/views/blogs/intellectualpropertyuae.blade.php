                <li><strong><span style="font-size:18px; position:relative; top:0px; margin-right:3px; margin-left:-2px;">2.</span>Doing an IP Search</strong><br> <p>Check before applying, whether your idea, name, or design is already registered.</p></li>
                <li><strong><span style="font-size:18px; position:relative; top:0px; margin-right:3px; margin-left:-2px;">3.</span>Preparation and Submission of Documents</strong><br> <p>This includes application forms, proof of creation, identity documents, and technical details if applying for patents.</p></li>
                <li><strong><span style="font-size:18px; position:relative; top:0px; margin-right:3px; margin-left:-2px;">4.</span>Pay the Fees</strong><br> <p>Each category of IP has its own government fee and timeline.</p></li>
                <li><strong><span style="font-size:18px; position:relative; top:0px; margin-right:3px; margin-left:-2px;">5.</span>Government Review</strong><br> <p>Authorities review your application, and in the case of trademarks, publish it in the official gazette for objections.</p></li>
                <li><strong><span style="font-size:18px; position:relative; top:0px; margin-right:3px; margin-left:-2px;">6.</span>Final Registration</strong><br> <p>AIn case no objections are raised, the IP is registered and protected.</p></li>
