    (function(){
  const stage = document.getElementById('tStage');
  const cards = Array.from(stage.querySelectorAll('.t-card'));
  const positions = ['pos1','pos2','pos3'];

  const q = document.getElementById('mainQuote');
  const n = document.getElementById('mainName');

  const prevBtn = document.getElementById('tPrev');
  const nextBtn = document.getElementById('tNext');

  const mql = window.matchMedia('(max-width: 991.98px)');
  let isMobile = mql.matches;

  // different speeds
  const MOBILE_AUTOPLAY_MS  = 3000;  // ⬅️ mobile speed
  const DESKTOP_AUTOPLAY_MS = 3000;  // ⬅️ desktop speed

  // Mobile track state
  let track = null, index = 0;
  let timer = null;

  function syncLeftFromEl(el){
    if(!el) return;
    q.textContent = el.querySelector('.t-text')?.textContent.trim() || '';
    n.textContent = el.querySelector('.t-name')?.textContent.trim() || '';
  }

  // -------- Desktop mode --------
  function rotateDesktop(dir){ // 1 next, -1 prev
    cards.forEach(card=>{
      const idx = positions.findIndex(p=>card.classList.contains(p));
      if(idx === -1) return;
      card.classList.remove(positions[idx]);
      const next = (idx + dir + positions.length) % positions.length;
      card.classList.add(positions[next]);
    });
    const active = stage.querySelector('.t-card.pos1');
    syncLeftFromEl(active);
  }

  // -------- Mobile mode (horizontal) --------
  function buildTrack(){
    if(track) return;
    track = document.createElement('div');
    track.className = 't-track';
    // move cards into track
    cards.forEach(c=> track.appendChild(c));
    stage.prepend(track);
    // first active
    index = 0;
    updateTrack(true);
    syncLeftFromEl(cards[index]);
    bindTouch();
  }

  function destroyTrack(){
    if(!track) return;
    while(track.firstChild) stage.appendChild(track.firstChild);
    track.remove(); track = null;
  }

  function slideTo(i){
    // loop all cards left->right
    if (i < 0) i = cards.length - 1;
    if (i >= cards.length) i = 0;
    index = i;
    updateTrack();
    syncLeftFromEl(cards[index]);
  }

  function next(){ slideTo(index + 1); }
  function prev(){ slideTo(index - 1); }

  function updateTrack(immediate=false){
    if(!track) return;
    const w = stage.getBoundingClientRect().width;
    if(immediate) track.style.transition = 'none';
    requestAnimationFrame(()=>{
      track.style.transform = `translate3d(${-index * w}px, 0, 0)`;
      if(immediate){
        track.getBoundingClientRect(); // reflow
        track.style.transition = '';
      }
    });
  }

  // touch swipe
  let startX = 0, deltaX = 0, dragging = false;
  function bindTouch(){
    if(!track) return;
    stage.addEventListener('touchstart', (e)=>{
      if(!isMobile) return;
      dragging = true; startX = e.touches[0].clientX; deltaX = 0; stop();
      track.style.transition = 'none';
    }, {passive:true});

    stage.addEventListener('touchmove', (e)=>{
      if(!dragging || !isMobile) return;
      deltaX = e.touches[0].clientX - startX;
      const w = stage.getBoundingClientRect().width;
      track.style.transform = `translate3d(${(-index * w) + deltaX}px, 0, 0)`;
    }, {passive:true});

    stage.addEventListener('touchend', ()=>{
      if(!dragging || !isMobile) return;
      dragging = false;
      const w = stage.getBoundingClientRect().width;
      const threshold = Math.max(50, w * 0.12);
      track.style.transition = ''; // restore
      if(deltaX >  threshold) prev();
      else if(deltaX < -threshold) next();
      else updateTrack(); // snap back
      start(); // resume autoplay
    });
  }

  // --- autoplay with different speeds ---
  function start(){
    stop();
    const ms = isMobile ? MOBILE_AUTOPLAY_MS : DESKTOP_AUTOPLAY_MS;
    timer = setInterval(()=> isMobile ? next() : rotateDesktop(1), ms);
  }
  function stop(){ if(timer){ clearInterval(timer); timer = null; } }

  // --- Mode switch ---
  function enterMobile(){
    // remove desktop pos classes effect
    cards.forEach(c=> positions.forEach(p=> c.classList.remove(p)));
    buildTrack();
    start();
  }
  function leaveMobile(){
    stop();
    destroyTrack();
    // restore desktop positions
    cards.forEach(c=> positions.forEach(p=> c.classList.remove(p)));
    cards[0] && cards[0].classList.add('pos1');
    cards[1] && cards[1].classList.add('pos2');
    cards[2] && cards[2].classList.add('pos3');
    syncLeftFromEl(cards[0] || null);
    start();
  }

  function onModeChange(){
    isMobile = mql.matches;
    if(isMobile) enterMobile(); else leaveMobile();
  }

  // first run
  if(isMobile) enterMobile(); else leaveMobile();

  // arrows
  prevBtn.addEventListener('click', ()=>{ stop(); isMobile ? prev() : rotateDesktop(-1); start(); });
  nextBtn.addEventListener('click', ()=>{ stop(); isMobile ? next() : rotateDesktop(1);  start(); });

  // click card behavior
  cards.forEach(card=>{
    card.addEventListener('click', ()=>{
      stop();
      if(isMobile){
        const idx = cards.indexOf(card);
        slideTo(idx);
      }else{
        if(card.classList.contains('pos2')) rotateDesktop(-1);
        else if(card.classList.contains('pos3')) rotateDesktop(1);
      }
      start();
    });
  });

  // hover pause desktop; ignore on mobile
  stage.addEventListener('mouseenter', ()=>{ if(!isMobile) stop(); });
  stage.addEventListener('mouseleave', ()=>{ if(!isMobile) start(); });

  document.addEventListener('visibilitychange', ()=> document.hidden ? stop() : start());
  mql.addEventListener('change', onModeChange);
  window.addEventListener('resize', ()=>{ 
    if(isMobile) updateTrack(true); 
  });
})();
